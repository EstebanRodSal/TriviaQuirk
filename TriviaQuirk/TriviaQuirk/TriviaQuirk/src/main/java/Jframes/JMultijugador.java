    public void cambiarColorPanelRespuesta(Byte numPanel, boolean esCorrecta) {
    PanelRound panelSeleccionado;
    switch (numPanel) {
        case 1: panelSeleccionado = panelOpcion1; break;
        case 2: panelSeleccionado = panelOpcion2; break;
        case 3: panelSeleccionado = panelOpcion3; break;
        default: return; // Si el número del panel no es válido, no hace nada
    }
    // Cambia el color del panel según si la respuesta es correcta o no
    panelSeleccionado.setBackground(esCorrecta ? Color.GREEN : Color.RED);
}

public void restablecerColorPanelRespuesta(Byte numPanel) {
    PanelRound panelSeleccionado;
    Color colorOriginal = new Color(255, 210, 63);
    switch (numPanel) {
        case 1: panelSeleccionado = panelOpcion1; break;
        case 2: panelSeleccionado = panelOpcion2; break;
        case 3: panelSeleccionado = panelOpcion3; break;
        default: return;
    }
    panelSeleccionado.setBackground(colorOriginal);
}

