    public void setPreguntaText(String newText) {
    int maxCaracteres = 38;
    int contadorCaracteres = newText.length();

    if (contadorCaracteres > maxCaracteres) {
        String textoReducido = newText.substring(0, contadorCaracteres); // Obtiene los primeros maxCaracteres caracteres
        lbPregunta.setText(textoReducido);
        // Ajusta el tamaño de la fuente aquí
        lbPregunta.setFont(new Font("Arial", Font.PLAIN, 14)); // Cambia el tamaño de la fuente según sea necesario
    } else {
        lbPregunta.setText(newText);
        // Restablece el tamaño de la fuente a su valor predeterminado
        lbPregunta.setFont(new Font("Arial", Font.PLAIN, 16)); // Tamaño de fuente predeterminado
    }
}
    
